    private byte getChecksum(byte[] buff, byte len, int startIndex)
    {
            byte chksum=0;
            int i=0;

            for(i=0 ; i<len ; i++)
            {
                    chksum+=buff[i + startIndex];
            }

            chksum = (byte) ((~chksum) + 1);

            return chksum;
    }
